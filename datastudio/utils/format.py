""" Functions used to format data."""

def scale_number(num, suffix="B"):
    """
    Scale a number such as bytes to its proper format
    e.g:
        1253656 => '1.20MB'
        1253656678 => '1.17GB'
    """
    factor = 1024
    for unit in ["", "K", "M", "G", "T", "P"]:
        if num < factor:
            return f"{num:.2f}{unit}{suffix}"
        num /= factor